CREATE TABLE RESERVATION(
    RES_NO NUMBER,
    RES_COM VARCHAR2(200), --complete
    SUB_NO VARCHAR2(200) NOT NULL, --subscriber
    RES_TIME TIMESTAMP, --SYSTIMESTAMP-INSERT
    MEMBER_NO NUMBER,
    RESTAURANT_NO NUMBER
);
