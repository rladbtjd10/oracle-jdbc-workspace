CREATE TABLE REVIEW(
    REVIEW_NO NUMBER,
    ID VARCHAR2(200),
    REVIEW_CONTENT VARCHAR2(200), --리뷰-리뷰내용
    GRADE NUMBER,
    REVIEW_DATE DATE DEFAULT SYSDATE,
    PARANTS_COMMENT_SEQ VARCHAR2(200),
    MEMBER_NO NUMBER,
    RESTAURANT_NO NUMBER
);

