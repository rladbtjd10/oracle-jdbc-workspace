 SELECT STUDENT_NO "학번", STUDENT_NAME "이름", ENTRANCE_DATE "입학년도"
 FROM TB_STUDENT
 WHERE DEPARTMENT_NO = '002'
 ORDER BY ENTRANCE_DATE;
 
 --2번
 SELECT PROFESSOR_NAME, PROFESSOR_SSN
 FROM TB_PROFESSOR
 WHERE LENGTH(PROFESSOR_NAME) != 3
 ORDER BY PROFESSOR_NAME;
 
 
 --3번
 SELECT PROFESSOR_NAME "교수이름", TRUNC(MONTHS_BETWEEN(TO_DATE(SUBSTR(PROFESSOR_SSN, 1, 6), 'YYMMDD'), SYSDATE) / 12) "나이"
 FROM TB_PROFESSOR
 WHERE PROFESSOR_SSN NOT LIKE '0%' AND SUBSTR(PROFESSOR_SSN,8,1) IN (1)
 ORDER BY PROFESSOR_SSN;
 
